public class Looping {
    public static void main(String[] args) {
        double i=2;
        int n=10;
        while( i<=n)
        {
            if(i%2==0)
            {
                System.out.println("it is even");
                   
        }
        else
        {
            System.out.println("it is odd");
        }
        i++;
        }
    }
    
}
